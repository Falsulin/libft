#include "libft.h"

int		ft_atoi(const char *str)
{
	int signe;
	int nbr;
	int i;

	i = 0;
	signe = 1;
	nbr = 0;
	while (ft_white_space(str[i]))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			signe = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nbr = (nbr * 10) + (str[i] - '0');
		i++;
	}
	return (nbr * signe);
}
